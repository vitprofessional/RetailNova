    <h2>üé´ Warranty Management</h2>
    
    <h3>Recording Warranties</h3>
    <p>When selling warranty-eligible products:</p>
    <ol>
        <li>During sale, mark "Include Warranty"</li>
        <li>Enter warranty details:
            <ul>
                <li>Warranty period (months)</li>
                <li>Warranty type (Manufacturer, Extended, Store)</li>
                <li>Coverage details</li>
                <li>Terms and conditions</li>
            </ul>
        </li>
        <li>Warranty linked to serial number</li>
        <li>Warranty card can be printed</li>
    </ol>

    <h3>Processing Warranty Claims</h3>
    
    <div class="step">
        <span class="step-number">1</span>
        Customer brings product with issue
    </div>

    <div class="step">
        <span class="step-number">2</span>
        Look up product by serial number
    </div>

    <div class="step">
        <span class="step-number">3</span>
        Verify warranty status:
        <ul>
            <li>Check expiration date</li>
            <li>Review coverage terms</li>
            <li>Validate conditions</li>
        </ul>
    </div>

    <div class="step">
        <span class="step-number">4</span>
        Create warranty claim service:
        <ul>
            <li>Link to original sale</li>
            <li>Document claim reason</li>
            <li>Mark as warranty service</li>
            <li>No charge for covered issues</li>
        </ul>
    </div>

    <div class="step">
        <span class="step-number">5</span>
        Process according to warranty terms:
        <ul>
            <li>Repair at no cost</li>
            <li>Replace with similar item</li>
            <li>Refund (if applicable)</li>
        </ul>
    </div>

