    </ul>

    <h3>Inventory Health</h3>
    <ul>
        <li><strong>Inventory Turnover:</strong> How quickly products are sold and replaced</li>
        <li><strong>Dead Stock:</strong> Products with no sales in the last 90 days</li>
        <li><strong>Stock Value:</strong> Total value of current inventory</li>
        <li><strong>Reorder Points:</strong> Number of products needing restocking</li>
    </ul>

    <h3>Customer Insights</h3>
    <ul>
        <li><strong>New vs. Returning:</strong> Ratio of new to repeat customers</li>
        <li><strong>Customer Lifetime Value:</strong> Average total purchases per customer</li>
        <li><strong>Active Customers:</strong> Customers who purchased in the last 30 days</li>
        <li><strong>Outstanding Receivables:</strong> Total amount owed by customers</li>
    </ul>

